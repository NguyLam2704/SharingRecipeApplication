package com.example.sharingrecipeapp.Classes;

public class Theme {
    String name, image;

    public Theme(){ }
    public Theme(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
